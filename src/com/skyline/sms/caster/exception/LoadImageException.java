package com.skyline.sms.caster.exception;

public class LoadImageException extends Exception {

	public LoadImageException() {
		super();
	}

	public LoadImageException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public LoadImageException(String message, Throwable cause) {
		super(message, cause);
	}

	public LoadImageException(String message) {
		super(message);
	}

	public LoadImageException(Throwable cause) {
		super(cause);
	}
	
	

}
